DROP TABLE IF EXISTS team_stats;

CREATE TABLE team_stats (
    team_id INTEGER NOT NULL,
    variable VARCHAR(250) NOT NULL,
    first_downs INTEGER NOT NULL,
    first_downs_by_penalty INTEGER NOT NULL,
    third_down_percentage NUMERIC NOT NULL,
    fourth_down_percentage NUMERIC NOT NULL,
    average_interception_yards NUMERIC NOT NULL,
    average_kickoff_return_yards NUMERIC NOT NULL,
    average_punt_return_yards NUMERIC NOT NULL,
    interceptions INTEGER NOT NULL,
    net_average_punt_yards NUMERIC NOT NULL,
    net_passing_yards INTEGER NOT NULL,
    net_passing_yards_per_game NUMERIC NOT NULL,
    passing_first_downs INTEGER NOT NULL,
    passing_touchdowns INTEGER NOT NULL,
    rushing_first_downs INTEGER NOT NULL,
    rushing_attempts INTEGER NOT NULL,
    rushing_touchdowns INTEGER NOT NULL,
    rushing_yards INTEGER NOT NULL,
    rushing_yards_per_game NUMERIC NOT NULL,
    total_offensive_plays INTEGER NOT NULL,
    total_points INTEGER NOT NULL,
    total_points_per_game NUMERIC NOT NULL,
    total_touchdowns INTEGER NOT NULL,
    total_offensive_yards INTEGER NOT NULL,
    yards_per_game NUMERIC NOT NULL,
    yards_per_pass_attempt NUMERIC NOT NULL,
    yards_per_rush_attempt NUMERIC NOT NULL,
    completed_passes INTEGER NOT NULL,
    attempted_passes INTEGER NOT NULL,
    field_goals_completed INTEGER NOT NULL,
    field_goals_attempted INTEGER NOT NULL,
    total_fumbles INTEGER NOT NULL,
    defensive_interception INTEGER NOT NULL,
    yards_after_interception INTEGER NOT NULL,
    total_kickoffs_received INTEGER NOT NULL,
    yards_off_kickoff_received INTEGER NOT NULL,
    total_punts_received INTEGER NOT NULL,
    yards_off_punts_received INTEGER NOT NULL,
    total_punts_kicked INTEGER NOT NULL,
    total_punt_yards INTEGER NOT NULL,
    total_defensive_sacks INTEGER NOT NULL,
    yards_lost_from_sacks INTEGER NOT NULL,
    total_penalties INTEGER NOT NULL,
    total_yards_penalized INTEGER NOT NULL
);
